const inputs = document.querySelectorAll("input");
const body = document.querySelector('.body');
var isColored = false;
inputs[0].addEventListener("click", function() {
    if (isColored) {
        body.style.background = '#504f4f';
        isColored = false;
    } else {
        body.style.background = 'linear-gradient(90deg, #504f4f, #dbdbdb)';
        body.style.backgroundSize = "400% 400%";
        body.style.animationName = "gradient";
        body.style.animationDuration = "10s";
        body.style.animationTimingFunction = "ease";
        body.style.animationIterationCount = "infinite";
        isColored = true;
    }
})
